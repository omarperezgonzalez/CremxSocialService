<p>VI. Ejes de intervención de CREmx</p>
</div>

<p class="X" style="text-align: center;">CREmx cuenta con 3 ejes de intervención para el apoyo de la mejora educativa con un enfoque basado en derechos humanos y equidad de oportunidades,<br>humanístico-educativo.<br><br></p>
<P> <b>Programa Proyecto de Vida</b><br><br>
    Programa de intervención social que opera en escuelas públicas de zonas vulnerables en Nuevo León.<br><br>
</p>

<p>
    <b>Revista Vanguardia Educativa</b><br><br>
    Programa de difusión cultural y educativa que lo realiza a través de la edición de la revista Vanguardia Educativa que llega a toda la República.<br><br>
    <b>Congreso Mundial de Educación</b><br><br>
    Programa de difusión y actualización de temas educativos a través del Congreso Mundial de Educación, los dos últimos ejes son iniciativas de incidencia.<br><br>
</p><br><br>
